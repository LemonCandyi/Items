package cn.yinmi.items.POJO;


import java.lang.reflect.Array;

public class Stocks {
    private Array item_name;
    private Array item_no;
    private Array number;

    public Array getItem_name() {
        return item_name;
    }

    public void setItem_name(Array item_name) {
        this.item_name = item_name;
    }

    public Array getItem_no() {
        return item_no;
    }

    public void setItem_no(Array item_no) {
        this.item_no = item_no;
    }

    public Array getNumber() {
        return number;
    }

    public void setNumber(Array number) {
        this.number = number;
    }
}
